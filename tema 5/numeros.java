public class numeros {
    public static void main(String[] args) {    
      

      
    for(int i = 0; i<=9; i++){
      
      if(i==3){
        
        System.out.print("E");
        
      }else{
        
        System.out.print(i+"-");
      }
    }  
    
    for(int j = 0; j<=9; j++){
      
        if(j==3){
          System.out.print("E");
          
        }else{
        
        System.out.print(j+"-");
      }
    
    }
    
    for(int n=0; n<=9; n++){
      
      if(n==3){
        
        System.out.print("E");
        
      }else{
        
        System.out.print(n+"\n");
      }
    
    }
    
  } 

  } 
 

